	/**
	 * Find the path from start to goal using breadth first search
	 * 
	 * @param start
	 *            The starting location
	 * @param goal
	 *            The goal location
	 * @param nodeSearched
	 *            A hook for visualization. See assignment instructions for how
	 *            to use it.
	 * @return The list of intersections that form the shortest (unweighted)
	 *         path from start to goal (including both start and goal).
	 */
	public List<GeographicPoint> bfs(GeographicPoint start, GeographicPoint goal,
			Consumer<GeographicPoint> nodeSearched) {

		// Get the start vertex
		GraphVertex startVertex = vertices.get(start);

		// Initialize search variables
		BFSearchStruct search = new BFSearchStruct();
		Map<GraphVertex, GraphVertex> parents = new HashMap<>();

		// Add the start vertex to the queue
		search.enqueue(startVertex);

		// While we have vertices to process
		while (search.hasNext()) {
			// Get the next vertex on the queue
			GraphVertex current = search.nextVertex();

			// If the current vertex is in the goal then we return the path
			if (current.getLocation().equals(goal)) {
				return getPathList(current, parents);
			}
			// For each child of the current vertex we try to add it to the
			// queue, if its successful we add the current vertex as its parent
			for (GraphEdge edge : current.getEdges()) {
				GraphVertex child = vertices.get(edge.getTo());
				if (search.enqueue(child)) {
					parents.put(child, current);
				}
			}
			nodeSearched.accept(current.getLocation());
		}

		return null;
	}

	/**
	 * Generate a path List based on the goal vertex and the parent Map
	 * 
	 * @param goalVertex
	 *            The goal GraphVertex
	 * @param parents
	 *            The parent Map
	 * @return The list of intersections from start to goal (including both
	 *         start and goal).
	 */
	private List<GeographicPoint> getPathList(GraphVertex goalVertex, Map<GraphVertex, GraphVertex> parents) {
		LinkedList<GeographicPoint> result = new LinkedList<>();
		GraphVertex node = goalVertex;
		while (node != null) {
			result.addFirst(node.getLocation());
			node = parents.get(node);
		}
		return result;
	}

